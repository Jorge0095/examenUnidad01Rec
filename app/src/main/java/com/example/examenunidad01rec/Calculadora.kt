package com.example.examenunidad01rec

class Calculadora {
    public var num1: Float = 0.0f
    public var num2: Float = 0.0f

    public fun suma(): Float {
        return num1 + num2
    }

    public fun resta(): Float {
        return num1 - num2
    }

    public fun multiplicacion(): Float {
        return num1 * num2
    }

    public fun dividision(): Float {
        return num1 / num2
    }
}